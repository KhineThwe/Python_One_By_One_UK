#1.Arithmetic Operators
#+ Addition
#- subtraction
#* multiplication
#/ Division
#// Floor Division
#% Modulo
#** Power

a = 7
b = 2
#addition
add_result = a + b
print("Addition Result: ",add_result)

#subtraction
sub_result = a - b
print("Subtraction Result: ",sub_result)

#multiplication
mult_result = a * b
print("Multiplication Result: ",mult_result)

#Division
div_result = a / b
print("Division Result: ",div_result)

#Floor Division
floor_result = a // b
print("Floor Division Result: ",floor_result)#quotient

#Modulo
mod_result = a % b
print("Modulo Result: ",mod_result)#Remainder

#power
pow_result = a ** b#7**2
print("Pow result: ",pow_result)

