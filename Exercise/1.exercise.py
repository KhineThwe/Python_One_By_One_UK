#binary number 0,1
#hexadecimal number 0-9A-F --> 16 digits
#decimal number --> single digit 10 --> 0-9
#octal number ---> 0-7

a = 50
#hexadecimal value
hex_a = hex(a)
print("Hexadecimal : ",hex_a)

#octal
octal_a = oct(a)
print("Octal value: ",octal_a)

#binary
bin_a = bin(a)
print("Binary value: ",bin_a)

